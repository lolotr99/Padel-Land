    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Boolean guardar = true;
        String mensaje = "";
        
        //Lo primero es recoger los campos del formulario.
        String nombre = tfNombre.getText();
        String apellidos = tfApellidos.getText();
        String username = tfUserName.getText();
        String password = new String(tfPassword.getPassword());
        String telefono = tfTelefono.getText();
        String imagenUsuario = "img/defecto.png";
        String rol = "basico";
        
        //A continuación se harían las validaciones pertinentes previas a insertarlo en la BD
        if (nombre.length() == 0){
            guardar = false;
            mensaje+= "El campo nombre no puede estar vacío \n";
        }
        if (apellidos.length() == 0){
            guardar = false;
            mensaje+= "El campo apellidos no puede estar vacío \n";
        }
        if (username.length() == 0){
            guardar = false;
            mensaje+= "El campo username no puede estar vacío";
        }
        if (password.length() == 0) {
            guardar = false;
            mensaje+= "El campo password no puede estar vacío";
        }
        if (telefono.length() == 0) {
            guardar = false;
            mensaje+= "El campo telefono no puede estar vacío";
        }
        
        if (guardar){
            UsuarioController userController = new UsuarioController();
            userController.insertarUsuario(nombre + apellidos, username, password, telefono, imagenUsuario, rol);
            tfNombre.setText("");
            tfApellidos.setText("");
            tfUserName.setText("");
            tfPassword.setText("");
            tfTelefono.setText("");
        } else{
            JOptionPane.showMessageDialog(null, mensaje);
        }
        
        //Aquí si todo ha ido bien insertamos el registro
    }//GEN-LAST:event_btnGuardarActionPerformed

