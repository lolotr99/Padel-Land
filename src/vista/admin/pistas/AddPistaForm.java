    private void jButtonElegirImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonElegirImagenActionPerformed
        // TODO add your handling code here:
        String path= null;
        //Elegimos una imagen y la seteamos en el label
        JFileChooser chooser = new JFileChooser();
        
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        //Extensión del archivo
        FileNameExtensionFilter extension = new FileNameExtensionFilter("*Images","jpg", "png", "jpeg");
        chooser.addChoosableFileFilter(extension);
        
        int filestate = chooser.showSaveDialog(null);
        
        //Comprobamos si el usuario ha seleccionado la imagen
        if (filestate == JFileChooser.APPROVE_OPTION){
            File selectedImage = chooser.getSelectedFile();
            path = selectedImage.getAbsolutePath();
            jLabelRutaImagenPista.setText(path);
            
            image_path = path;
        }
    }//GEN-LAST:event_jButtonElegirImagenActionPerformed

    //Se crea un método para verificar y validar los campos
    public boolean verifyFields() {
        String nombrePista = jTextFieldNombrePista.getText();
        
        if (nombrePista.trim().equals("")){
            JOptionPane.showMessageDialog(null, "El nombre de la pista es obligatorio","Campo vacío",2);
            return false;
        } else if (image_path == null){
            JOptionPane.showMessageDialog(null, "Selecciona una imagen de pista", "Campo vacío", 2);
            return false;
        } else{
            return true;
        }
    }
    
